package com.itheima.d5_Test5;

public class NodeAndIndex {
    private Node node;
    private int index;

    public NodeAndIndex() {
    }

    public NodeAndIndex(Node node, int index) {
        this.node = node;
        this.index = index;
    }

    public Node getNode() {
        return node;
    }

    public void setNode(Node node) {
        this.node = node;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
